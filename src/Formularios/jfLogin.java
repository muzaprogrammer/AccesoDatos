    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        // TODO add your handling code here:
        try{
            Connection conex = ConexionSql.getInstancia().getConexion();
            CallableStatement sql = conex.prepareCall("{call SP_ValidarUsuario(?,?,?)}");
            sql.setString(1, tfLogin.getText());
            sql.setString(2, pfPassword.getText());
            sql.setString(3, "Pr09r@3");
            ResultSet rs = sql.executeQuery();
            if (rs.next()){//Si devuelve datos
                if (rs.getInt(1) > 0){
                    JOptionPane.showMessageDialog(null, "Bienvenido al Sistema Id: " + rs.getInt(1) + " - " + rs.getString(4));
                    //Invocar otro form
                    this.dispose();
                    jfMenu menu = new jfMenu();
                    menu.setVisible(true);
                }
                else
                    JOptionPane.showMessageDialog(null, "Usuario o clave inv√°lidos");
            }
            else
                JOptionPane.showMessageDialog(null, "Error en consulta");
        }   
        catch (Exception ex){
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_btnIngresarActionPerformed

